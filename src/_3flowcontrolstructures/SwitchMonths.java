package _3flowcontrolstructures;

public class SwitchMonths {
    public static void main(String[] args) {
        String month="May";
        switch(month){
            case "January":
                System.out.println("The month is "+month +" and it is the first month of the year");
                break;
            case "February":
                System.out.println("The month is "+month+" and it is the second month of the year");
                break;
            case "March":
                System.out.println("The month is "+month+" and it is the third month of the year");
                break;
            case "April":
                System.out.println("The month is "+month+" and it is the fourth month of the year");
                break;
            case "May":
                System.out.println("The month is "+month+" and it is the fifth month of the year");
                break;
            case "June":
                System.out.println("The month is "+month+" and it is the sixth month of the year");
                break;
            case "July":
                System.out.println("The month is "+month+" and it is the seventh month of the year");
                break;
            case "August":
                System.out.println("The month is "+month+" and it is the eighth month of the year");
                break;
            case "September":
                System.out.println("The month is "+month+" and it is the ninth month of the year");
                break;
            case "October":
                System.out.println("The month is "+month+" and it is the tenth month of the year");
                break;
            case "November":
                System.out.println("The month is "+month+" and it is the eleventh month of the year");
                break;
            case "December":
                System.out.println("The month is "+month+" and it is the twelfth month of the year");
                break;
        }
    }
}
